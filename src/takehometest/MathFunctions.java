package takehometest;

import static java.lang.Math.sqrt;


public class MathFunctions {
    
    public static double SquareRootAndAddThree(int input) {
         double j = 0;
        if(input >= 0){
             j = sqrt(input) + 3;
  
        }
      return j; 
    }
}
